package question1;

/**
 * This class represents a node in a linked list
 * @author Jugal
 *
 */
public class Node {

	 int data;
	 Node next;
	
	/**
	 * Constructor to initialize the data members of the class
	 * @param data
	 */
	public Node(int data)
	{
		this.data = data;
		this.next = null;
	}
}
